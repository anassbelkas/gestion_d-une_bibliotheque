package gestion.bibliotheque.projet.vo;

public class AchatLivreVo {

    private String ref;
    private String prixTotal;
    private String prixMin;
    private String prixMax;

    public String getRef() {
        return ref;
    }

    public void setRef(String ref) {
        this.ref = ref;
    }

    public String getPrixTotal() {
        return prixTotal;
    }

    public void setPrixTotal(String prixTotal) {
        this.prixTotal = prixTotal;
    }

    public String getPrixMin() {
        return prixMin;
    }

    public void setPrixMin(String prixMin) {
        this.prixMin = prixMin;
    }

    public String getPrixMax() {
        return prixMax;
    }

    public void setPrixMax(String prixMax) {
        this.prixMax = prixMax;
    }

}
